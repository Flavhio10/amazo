    private void btnSalvarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarLivroActionPerformed
        try {
            long isbn = Long.parseLong(this.txtIsbnLivro.getText());
            String titulo = this.txtTituloLivro.getText();
            String subtitulo = this.txtSubTituloLivro.getText();
            String edicao = this.txtEdicaoLivro.getText();
            int paginas = Integer.parseInt(this.txtPaginasLivro.getText());
            String local = this.txtLocalLivro.getText();
            String editora = this.txtEditoraLivro.getText();
            int ano = Integer.parseInt(this.txtAnoLivro.getText());
            new ControleLivro().cadastrar(new Livro(isbn, titulo, subtitulo, edicao, paginas, local, editora, ano));
            this.tabelaLivros.setToolTipText("");
            this.txtAnoLivro.setText("");
            this.txtEdicaoLivro.setText("");
            this.txtEditoraLivro.setText("");
            this.txtIsbnLivro.setText("");
            this.txtLocalLivro.setText("");
            this.txtPaginasLivro.setText("");
            this.txtSubTituloLivro.setText("");
            this.txtTituloLivro.setText("");
            lerTabela();
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, new NumberFormatException("Dados inválidos para os campos").getMessage());
        }

    }//GEN-LAST:event_btnSalvarLivroActionPerformed

