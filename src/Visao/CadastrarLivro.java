    public void lerTabela() {

        List<Livro> listLivros = new ArrayList<>();
        listLivros.addAll(new LivroDao().listarTodos());

        DefaultTableModel modelo = (DefaultTableModel) this.tabelaLivros.getModel();
        this.tabelaLivros.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);

        for (Livro livro : listLivros) {
            modelo.addRow(new Object[]{livro.getId(), livro.getIsbn(), livro.getTitulo(), livro.getSubtitulo(),
                livro.getEdicao(), livro.getPaginas(), livro.getLocal(), livro.getEditora(), livro.getAno()});
        }
    }

